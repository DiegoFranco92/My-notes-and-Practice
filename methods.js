
//Los "methods" son funciones que aplican en tipos de datos "Data Type" ya sea un string, o un objeto por ejemplo:
const MiNombre = "Diego Alberto Franco Torres"

//le aplicaremos a la "variable" "MiNombre" un "method":
MiNombre.toUpperCase()
//el resultado sera la "variable" "MiNombre" pero con todos sus caracteres en MAYUSCULAS.
// A los "methods" se les define siempre con un "." seguido del tipo de dato en el que se le desea ejecutar.
